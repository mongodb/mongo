#pragma once

#include "mongo/db/query/compiler/optimizer/join/join_reordering_context.h"
#include "mongo/db/query/compiler/optimizer/join/plan_enumerator.h"
#include "mongo/util/modules.h"

namespace mongo::join_ordering {

struct ReorderedJoinSolution {
    std::unique_ptr<QuerySolution> soln;
    // Base node of new join order.
    NodeId baseNode;
};

/**
 * Construct a 'QuerySolution' with a pseudo-random join ordering based on the given seed. Performs
 * a random walk of the join graph in 'ctx' to determine the join order and uses the given
 * 'QuerySolutionMap' to lookup the corresponding single-table access plan for each node.
 * 'defaultHJ' specifies if a hash join should be the default instead of a nested loop join if an
 * INLJ is not possible.
 */
ReorderedJoinSolution constructSolutionWithRandomOrder(const JoinReorderingContext& ctx,
                                                       int seed,
                                                       bool defaultHJ = false);

/**
 * Construct a 'QuerySolution' with a join ordering based on the result generated by bottom-up
 * Sellinger-style join optimization.
 */
ReorderedJoinSolution constructSolutionBottomUp(const JoinReorderingContext& ctx,
                                                PlanTreeShape shape);

}  // namespace mongo::join_ordering
